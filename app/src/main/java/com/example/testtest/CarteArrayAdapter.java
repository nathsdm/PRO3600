package com.example.testtest;

import android.content.Context;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.ArrayAdapter;
import android.widget.TextView;

import androidx.annotation.NonNull;
import androidx.annotation.Nullable;

import java.util.List;

public class CarteArrayAdapter extends ArrayAdapter<Carte> {

    public CarteArrayAdapter(@NonNull Context context, int resource, @NonNull List<Carte> objects) {
        super(context, resource, objects);
    }

    @NonNull
    @Override
    public View getView(int position, @Nullable View convertView, @NonNull ViewGroup parent) {
        if (convertView == null) {
            convertView = LayoutInflater.from(getContext()).inflate(android.R.layout.simple_dropdown_item_1line, parent, false);
        }

        TextView textView = convertView.findViewById(android.R.id.text1);
        Carte carte = getItem(position);

        if (carte != null) {
            textView.setText(carte.getName());
        }

        return convertView;
    }
}

